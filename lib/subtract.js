'use strict';

module.exports = subtract;

/**
 * Subtract one number from another.
 *
 * @param {number} a
 * @param {number} b The number to minus from the first.
 *
 * @returns {number} The result of the subtraction operation.
 *
 * @example
 * subtract(10, 3);
 * // returns 7
 */

function subtract(a, b) {
  return a - b;
}
